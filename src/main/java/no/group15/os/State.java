package no.group15.os;

/**
 * The state of the barber.
 * @author Group 13
 * @version 0.1
 */
public enum State {

    /**
     * Represents the state when a person sleeps.
     */
    SLEEP,

    /**
     * Represents the state when a person is working
     */
    WORKING,

    /**
     * Represents the state when a person is done at work.
     */
    FREEDOM,

    /**
     * Represents the state when the barber looks for work.
     */
    LOOKINGFORWORK
}
